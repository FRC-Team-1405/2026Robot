    /**
     * Gets the filtered linear acceleration magnitude of the robot.
     * 
     * @return Linear acceleration in m/s²
     */
    public double getFilteredAcceleration() {
        ChassisSpeeds currentSpeeds = getState().Speeds;
        double currentTime = edu.wpi.first.wpilibj.Timer.getFPGATimestamp();
        double dt = currentTime - m_lastSpeedsTime;

        if (dt == 0.0 || m_lastSpeedsTime == 0.0) {
            // First call or no time elapsed
            m_lastSpeeds = currentSpeeds;
            m_lastSpeedsTime = currentTime;
            return 0.0; // Return zero acceleration
        }

        // Calculate raw acceleration
        double ax = (currentSpeeds.vxMetersPerSecond - m_lastSpeeds.vxMetersPerSecond) / dt;
        double ay = (currentSpeeds.vyMetersPerSecond - m_lastSpeeds.vyMetersPerSecond) / dt;

        // Apply filtering
        ax = m_axFilter.calculate(ax);
        ay = m_ayFilter.calculate(ay);

        // Update for next call
        m_lastSpeeds = currentSpeeds;
        m_lastSpeedsTime = currentTime;

        // Return magnitude of linear acceleration
        return Math.hypot(ax, ay);
    }

